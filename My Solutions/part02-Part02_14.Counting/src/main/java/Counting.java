
import java.util.Scanner;

public class Counting {

    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        
        //Taking input from user
        int num = scanner.nextInt();
        
        // Loop start
        for (int i = 0; i <= num;  i++) {
            System.out.println(i);
        }
    }
}
